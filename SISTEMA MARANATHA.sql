CREATE TABLE CREDITO (
	IDCREDIO INT IDENTITY (1,1) PRIMARY KEY,
	CODIGO AS ('VEN'+RIGHT ('00'+ CONVERT(VARCHAR, IDCREDIO),(2))),
	FECHA DATE,
	TOTAL_VENTA DECIMAL(18,2),
	INTERES DECIMAL (8,2),
	TIEMPO INT,
	CUOTAS_PAGADAS INT,
	CUOTA DECIMAL(18,2),
